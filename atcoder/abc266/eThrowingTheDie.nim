proc solve(x: int): float =
  if x == 1: return 3.5
  let b = solve(x-1)
  let q = 6 - b.ceil().toInt() + 1
  # echo (x, q)
  let ok = q/6 * ([1, 2, 3, 4, 5, 6][^q..^1].sum() / q)
  let ng = (6-q)/6*b
  # echo (x, (ok, ng), b)
  return ok + ng

